import UIKit

extension UIColor {
    static var plasticColor: UIColor { return UIColor(named: "plasticColor")!}
    static var wastepaperColor: UIColor { return UIColor(named: "wastepaperColor")!}
    static var glassColor: UIColor { return UIColor(named: "glassColor")!}
    static var metalColor: UIColor { return UIColor(named: "metalColor")!}
    static var compositeColor: UIColor { return UIColor(named: "compositeColor")!}
    static var otherColor: UIColor { return UIColor(named: "otherColor")!}
}
